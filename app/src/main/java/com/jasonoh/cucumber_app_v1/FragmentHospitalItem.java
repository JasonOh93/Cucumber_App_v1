package com.jasonoh.cucumber_app_v1;

public class FragmentHospitalItem {
    String hospitalName, hospitalAddress, hospitalLatitude, hospitalLongitude, hospitalTelNum;

    public FragmentHospitalItem() {
    }

    public FragmentHospitalItem(String hospitalName, String hospitalAddress, String hospitalLatitude, String hospitalLongitude, String hospitalTelNum) {
        this.hospitalName = hospitalName;
        this.hospitalAddress = hospitalAddress;
        this.hospitalLatitude = hospitalLatitude;
        this.hospitalLongitude = hospitalLongitude;
        this.hospitalTelNum = hospitalTelNum;
    }
}//FragmentHospitalItem class
